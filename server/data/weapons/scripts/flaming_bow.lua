condition:addDamage(1, 2000, -13)
condition:addDamage(1, 2000, -12)
condition:addDamage(1, 2000, -11)
condition:addDamage(1, 2000, -10)
condition:addDamage(1, 2000, -9)
condition:addDamage(1, 2000, -8)
condition:addDamage(1, 2000, -7)
condition:addDamage(1, 2000, -6)
condition:addDamage(1, 2000, -5)
condition:addDamage(1, 2000, -4)
condition:addDamage(1, 2000, -3)
condition:addDamage(1, 2000, -2)
condition:addDamage(1, 2000, -1)
combat:setCondition(condition)

function onUseWeapon(player, variant)
	return combat:execute(player, variant)
end